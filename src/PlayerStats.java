public class PlayerStats {
    private int shotsFired;
    private int hits;
    private int misses;
    private int shipsRemaining;
    
    public int getShotsFired() {
        return shotsFired;
    }
    public int getHits() {
        return hits;
    }
    public int getMisses() {
        return misses;
    }
    public int getShipsRemaining() {
        return shipsRemaining;
    }

}
